let updatedItems = 0
let addedVarieties = 0

for (const dataset of datasets) {
  for (const item of dataset.data) {
    const additions = catalog[item.slug]
    if (!additions || item.category !== 'fruit') continue

    const existing = new Map((item.varieties || []).map(variety => [variety.name, variety]))
    for (const variety of additions) {
      if (existing.has(variety.name)) continue
      existing.set(variety.name, variety)
      addedVarieties += 1
    }
    item.varieties = [...existing.values()]
    updatedItems += 1
  }

  fs.writeFileSync(dataset.path, JSON.stringify(dataset.data, null, 2) + '\n')
}

console.log(`Updated fruit items: ${updatedItems}`)
console.log(`Added varieties: ${addedVarieties}`)

function v(name, season, traits, origin, note) {
  return { name, season, traits, origin, note }
}
